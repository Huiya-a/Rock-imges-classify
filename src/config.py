# -*- coding: utf-8 -*-
"""
配置文件 - 管理所有超参数和设置

使用说明：
    1. 本文件定义了所有训练相关的超参数
    2. 可通过命令行参数覆盖部分配置
    3. 建议使用命令行参数进行实验，而非直接修改此文件
    4. 配置类支持在运行时动态修改参数

配置优先级：命令行参数 > 代码修改 > 默认值
"""

import torch

class Config:
    """
    配置类 - 统一管理所有训练和模型参数

    配置分类：
        - 数据路径配置：数据集和模型保存位置
        - 数据参数：图像尺寸、批次大小等
        - 训练参数：训练轮数、学习率等
        - 学习率调度：学习率衰减策略
        - 早停机制：防止过拟合的参数
        - 数据增强：图像增强策略
        - 模型参数：模型架构和预训练设置
        - 集成学习：多模型集成配置
        - 损失函数：训练损失函数选择
        - 优化器：优化算法配置
        - 系统设置：设备、混合精度等
    """

    # ==================== 数据路径配置 ====================
    """数据集和模型文件的存储路径配置"""

    # 数据集根目录（相对路径）
    DATA_ROOT = '../data/rock-data'
    # 训练集路径
    TRAIN_DIR = '../data/rock-data/train'
    # 测试集路径
    TEST_DIR = '../data/rock-data/test'
    # 验证集路径
    VALID_DIR = '../data/rock-data/valid'

    # ==================== 模型保存路径配置 ====================
    """训练过程中模型和结果的保存位置"""

    # 训练模型保存目录
    MODEL_SAVE_DIR = 'models'
    # 训练结果保存目录（图表、报告等）
    RESULTS_DIR = 'results'

    # ==================== 数据参数配置 ====================
    """数据加载和预处理的参数设置"""

    # 输入图像尺寸（注意：InceptionV3需要299，其他模型通常224）
    IMAGE_SIZE = 224
    # 批次大小（根据GPU显存调整：8-128，显存不足时减小）
    BATCH_SIZE = 32
    # 数据加载工作进程数（建议设置为CPU核心数的一半）
    NUM_WORKERS = 4
    # 分类类别数量（岩石类型总数）
    NUM_CLASSES = 9

    # ==================== 训练参数配置 ====================
    """核心训练超参数，直接影响模型性能"""

    # 最大训练轮数（可根据需要调整：快速测试=5，完整训练=30-100）
    EPOCHS = 100
    # 初始学习率（推荐范围：0.0001-0.01，预训练模型通常使用较小值）
    LEARNING_RATE = 0.001
    # 权重衰减系数（L2正则化，防止过拟合，推荐值：1e-4 到 1e-5）
    WEIGHT_DECAY = 1e-4

    # ==================== 学习率调度配置 ====================
    """学习率衰减策略，帮助模型收敛到更好的解"""

    # 调度器类型（可选：'step'阶梯衰减, 'cosine'余弦退火, 'plateau'自适应）
    LR_SCHEDULER = 'cosine'
    # Step调度器：每隔多少轮衰减一次学习率
    LR_STEP_SIZE = 30
    # 学习率衰减因子（每个衰减周期乘以该因子，推荐0.1或0.5）
    LR_GAMMA = 0.1

    # ==================== 早停机制配置 ====================
    """防止过拟合的策略，当验证集性能不再提升时提前停止训练"""

    # 是否启用早停机制
    EARLY_STOPPING = True
    # 耐心值：验证损失不降低的容忍轮数（推荐7-20轮）
    PATIENCE = 10
    # 最小改进阈值：验证损失降低幅度小于此值时不视为改进
    MIN_DELTA = 0.001

    # ==================== 设备配置 ====================
    """计算设备选择，优先使用GPU加速训练"""

    # 自动检测并使用CUDA设备（GPU），否则使用CPU
    DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    # ==================== 数据增强配置 ====================
    """
    数据增强策略，增加数据多样性，提高模型泛化能力

    参数说明：
        - rotation: 随机旋转角度范围（度）
        - horizontal_flip/vertical_flip: 水平/垂直翻转概率
        - brightness/contrast/saturation: 亮度/对比度/饱和度调整幅度
        - hue: 色调调整幅度
        - gaussian_blur: 高斯模糊概率
        - random_crop: 随机裁剪比例
        - color_jitter: 颜色抖动强度
    """

    AUGMENTATION = {
        'rotation': 45,              # 随机旋转：±45度（增强）
        'horizontal_flip': 0.5,      # 50%概率水平翻转
        'vertical_flip': 0.5,        # 50%概率垂直翻转（增强）
        'brightness': 0.3,           # 亮度调整：±30%（增强）
        'contrast': 0.3,             # 对比度调整：±30%（增强）
        'saturation': 0.2,           # 饱和度调整：±20%
        'hue': 0.1,                  # 色调调整：±0.1
        'gaussian_blur': 0.5,        # 50%概率应用高斯模糊（增强）
        'random_crop': 0.7,          # 70%概率随机裁剪（增强，更多裁剪）
        'color_jitter': 0.4          # 颜色抖动强度
    }

    # ==================== 高级数据增强配置 ====================
    """
    Mixup和CutMix数据增强配置

    Mixup: 通过线性插值混合两张图像和标签
    CutMix: 通过裁剪区域替换混合两张图像和标签

    注意：Mixup和CutMix不能同时启用（当前实现）
    
    实验结论：
    - Mixup和CutMix都不适合岩石分类任务
    - Mixup: 测试准确率70.11%（低于基线71.84%）
    - CutMix: 测试准确率70.69%（低于基线71.84%）
    - 结论：禁用Mixup和CutMix，保持基线
    """

    # Mixup配置
    USE_MIXUP = False  # 是否使用Mixup增强（已禁用，效果不佳）
    MIXUP_ALPHA = 0.4  # Mixup的alpha参数（控制混合强度）

    # CutMix配置
    USE_CUTMIX = False  # 是否使用CutMix增强（已禁用，效果不佳）
    CUTMIX_BETA = 1.0  # CutMix的beta参数（控制裁剪区域大小）

    # ==================== 模型参数配置 ====================
    """模型架构和训练相关的参数设置"""

    # 模型类型（可选：custom_cnn, resnet18/34/50/101, efficientnet_b0/b1/b2, inception_v3, vgg11/13/16）
    MODEL_TYPE = 'resnet50'
    # 是否使用ImageNet预训练权重（True=迁移学习，False=从零训练）
    PRETRAINED = True
    # Dropout丢弃率（防止过拟合，推荐0.3-0.7）
    DROPOUT_RATE = 0.6

    # ==================== 集成学习配置 ====================
    """多模型集成策略，通过组合多个模型提升预测准确率"""

    # 是否启用集成学习模式
    ENSEMBLE = True
    # 集成模型列表（建议选择不同架构的模型以获得更好的多样性）
    ENSEMBLE_MODELS = ['resnet50', 'inception_v3', 'efficientnet_b0']

    # ==================== 损失函数配置 ====================
    """训练过程中的损失函数选择"""

    # 损失函数类型（可选：'cross_entropy'交叉熵, 'focal_loss'焦点损失, 'label_smoothing'标签平滑）
    LOSS_FUNCTION = 'label_smoothing'
    # 标签平滑系数（仅在label_smoothing模式下生效，推荐0.1）
    LABEL_SMOOTHING = 0.1

    # ==================== 优化器配置 ====================
    """优化算法选择和参数配置"""

    # 优化器类型（可选：'adam'自适应矩估计, 'adamw'带权重衰减的Adam, 'sgd'随机梯度下降）
    OPTIMIZER = 'adamw'
    # SGD优化器的动量参数（仅在SGD模式下生效，推荐0.9）
    MOMENTUM = 0.9

    # ==================== 系统设置 ====================
    """训练过程中的系统级优化设置"""

    # 是否使用混合精度训练（仅CUDA可用，可提升训练速度和减少显存占用）
    MIXED_PRECISION = True

    # ==================== 可视化配置 ====================
    """训练过程可视化的相关设置"""

    # 绘图间隔（每多少轮保存一次图表）
    PLOT_INTERVAL = 5
    # 是否保存训练图表（混淆矩阵、准确率曲线等）
    SAVE_PLOTS = True

    # ==================== 验证配置 ====================
    """验证集划分策略"""

    # 验证集比例（当没有单独验证集时，从训练集中划分，推荐0.1-0.3）
    VALIDATION_SPLIT = 0.2

    @classmethod
    def print_config(cls):
        """
        打印当前所有配置信息

        用途：
            - 调试时查看当前配置
            - 记录实验配置以便复现
            - 验证参数是否正确设置
        """
        print("=" * 50)
        print("配置信息:")
        print("=" * 50)
        for attr in dir(cls):
            if not attr.startswith('_') and not callable(getattr(cls, attr)):
                print(f"{attr}: {getattr(cls, attr)}")
        print("=" * 50)
